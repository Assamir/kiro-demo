package com.insurance.backoffice.domain;

/**
 * Enumeration representing the types of insurance offered.
 */
public enum InsuranceType {
    /**
     * Obligatory Civil Liability (OC) insurance - mandatory coverage as per law.
     */
    OC,
    
    /**
     * Autocasco (AC) insurance - comprehensive vehicle coverage.
     */
    AC,
    
    /**
     * Accidents (NNW) insurance - personal accident coverage.
     */
    NNW
}